// 卦象显示组件
const GuaSymbol = ({ guaNumber, upperGua, lowerGua, className = "" }: { 
  guaNumber: number, 
  upperGua: string, 
  lowerGua: string, 
  className?: string 
}) => {
  // 基础八卦的爻线配置（从下往上）
  const basicGua: { [key: string]: boolean[] } = {
    '乾': [true, true, true],    // 三个阳爻
    '坤': [false, false, false], // 三个阴爻
    '震': [true, false, false],  // 下阳上阴阴
    '巽': [false, true, true],   // 下阴上阳阳
    '坎': [false, true, false],  // 上下阴中阳
    '离': [true, false, true],   // 上下阳中阴
    '艮': [false, false, true],  // 下阴阴上阳
    '兑': [true, true, false],   // 下阳阳上阴
  }

  // 获取六爻配置（从下往上）
  const lowerLines = basicGua[lowerGua] || [false, false, false]
  const upperLines = basicGua[upperGua] || [false, false, false]
  const allLines = [...lowerLines, ...upperLines]

  // 渲染爻线
  const renderLine = (isYang: boolean, index: number) => (
    <div key={index} className="w-full h-1 mb-1 last:mb-0">
      {isYang ? (
        // 阳爻 - 实线
        <div className="w-full h-full bg-gray-800 rounded-sm"></div>
      ) : (
        // 阴爻 - 虚线（中间断开）
        <div className="w-full h-full flex gap-1">
          <div className="flex-1 h-full bg-gray-800 rounded-sm"></div>
          <div className="flex-1 h-full bg-gray-800 rounded-sm"></div>
        </div>
      )}
    </div>
  )

  return (
    <div className={`relative ${className}`}>
      {/* 卦象（六个爻线，从上往下显示，所以要反转数组） */}
      <div className="w-12 h-16 p-2 bg-gray-50 rounded border border-gray-200 flex flex-col justify-center relative">
        {allLines.slice().reverse().map((isYang, index) => renderLine(isYang, index))}
        
        {/* 卦序数字 */}
        <div className="absolute bottom-0 left-0 text-xs font-bold text-purple-600 leading-none">
          {guaNumber}
        </div>
      </div>
    </div>
  )
}

